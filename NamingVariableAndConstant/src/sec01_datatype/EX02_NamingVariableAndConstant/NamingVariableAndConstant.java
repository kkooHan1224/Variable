package sec01_datatype.EX02_NamingVariableAndConstant;

public class NamingVariableAndConstant {
	public static void main(String[] args) {
		// 변수
		boolean aBcD;
		byte 가나다;
		short _abcd;
		char $ab_cd;
		// int 3abcd;
		long abcd3;
		// float int;
		double main;
		// int my Work;
		String myClassName;
		int ABC;
		
		
		// 상수
		final double PI;
		final int MY_DATA;
		final float myData;		// 소문자 사용(권장하지 않음)
	}

}
